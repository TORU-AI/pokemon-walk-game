export function getQuestionsByDifficulty(difficulty: 1 | 2 | 3 | 4 | 5): Question[] {
  return questions.filter((q) => q.difficulty === difficulty);
}

export function getDifficultyFromLevel(level: number): 1 | 2 | 3 | 4 | 5 {
  if (level <= 10) return 1;
  if (level <= 20) return 2;
  if (level <= 30) return 3;
  if (level <= 45) return 4;
  return 5;
}

export function getRandomQuestion(level: number, excludeIds: string[] = []): Question {
  const difficulty = getDifficultyFromLevel(level);
  let pool = getQuestionsByDifficulty(difficulty).filter((q) => !excludeIds.includes(q.id));
  if (pool.length === 0) pool = getQuestionsByDifficulty(difficulty);
  return pool[Math.floor(Math.random() * pool.length)];
}
